package week1.day2;

public class LearnDataType {

	public static void main(String[] args) {
		byte variableOne=50;
		short variableTwo=10000;
		int variableThree=50000;
		long phoneNum=9944540357l;
		float decimalExp=230.8657885788f;
		char letter='a';
		boolean condition=true;
		 
		
		System.out.println("My data type is byte :"+variableOne);
		System.out.println("My data type is short :"+variableTwo);
		System.out.println("My data type is int :"+variableThree);
		System.out.println("My data type is long :"+phoneNum);
		System.out.println("My data type is char :"+letter);
		System.out.println("My data type is float :"+decimalExp);
		System.out.println("My type is boolean :"+condition);
	}

}
